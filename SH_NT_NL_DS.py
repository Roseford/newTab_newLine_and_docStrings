# TRIPLE QUOTATION IS ALSO KNOWN AS DOCUMENT STRINGS.
# THESE ARE HOW DOCSTRINGS ARE USED.
print('''These are my cousin;
            onyi
            chidinm
            chinenye''')

# \n means new line while \t means new tab
print('These are my cousins; \n\t\t\t'
      'onyi \n\t\t\t'
      'chidinm\n\t\t\t'
      'chinenye')